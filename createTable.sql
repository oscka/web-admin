CREATE TABLE TB_USER_INFO (
USER_NO        INT             NOT NULL,
USER_ID        VARCHAR(30)     NOT NULL,
USER_NM        VARCHAR(50)     NOT NULL,
USER_PW        VARCHAR(100)    NOT NULL,
USER_CD        VARCHAR(30)     NOT NULL,
EMAIL          VARCHAR(30)     NOT NULL,
ADDRESS        VARCHAR(150)    NOT NULL,
TELPHONE       VARCHAR(30)     NOT NULL,
DEPT_NO        VARCHAR(30)     NOT NULL, -- 부서정보(TB_DEPT_INFO의 DEPT_NO)
JOB            VARCHAR(10)     NOT NULL, -- 업무 AA/SA
GENDER         CHAR(1)         NOT NULL, -- 성별 M/W
ROLE_ID        VARCHAR(20)     NOT NULL, -- 권한 ROLE_ADMIN/ROLE_USER  (TB_ROLE_INFO의 ROLE_ID)
USE_YN         CHAR(1)         NOT NULL, -- 계정사용유무 Y/N
FIRST_REG_ID   VARCHAR(30)     NOT NULL, -- 최초등록자ID
FIRST_REG_DT   TIMESTAMP       NOT NULL, -- 최초등록일시
LAST_CHANGE_ID VARCHAR(30)     NULL,     -- 최종변경자ID
LAST_CHANGE_DT TIMESTAMP       NULL,     -- 최종변경일시
PRIMARY KEY(USER_ID),
CONSTRAINT PK_DEPT_NO FOREIGN KEY(DEPT_NO) REFERENCES TB_COMMON_CODE(CODE_ID)
,FOREIGN KEY(ROLE_ID) REFERENCES TB_COMMON_CODE(CODE_ID)
);

